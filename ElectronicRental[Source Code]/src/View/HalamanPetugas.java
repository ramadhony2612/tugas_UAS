    private void setTableMember(){
        if(mController.getAllMember()==null){
            JOptionPane.showMessageDialog(this,"Daftar Member Tidak Dapat diload");
        }else{
            //resetTable();
            for(Member u : mController.getAllMember()){
                Object[] o = new Object[6];
                o[0] = u.getId();
                o[1] = u.getNIK();
                o[2] = u.getNama();
                o[3] = u.getTempat();
                o[4] = u.getTanggal();
                o[5] = u.getJK();
                tabelModel.addRow(o);
            }
            this.tabelMember.setModel(tabelModel);
        }
    }
    
    private void setTablePinjam(){
        if(mController.getAllMember()==null){
            JOptionPane.showMessageDialog(this,"Daftar Peminjaman Tidak Dapat diload");
        }else{
            //resetTable();
            for(Peminjaman p : pController.getAllPeminjaman()){
                Object[] o = new Object[11];
                o[0] = p.getId();
                o[1] = p.getIdBarang();
                o[2] = p.getIdUser();
                o[3] = p.getDurasi();
                o[4] = p.getTglPinjam();
                o[5] = p.getTglKemabli();
                o[6] = p.getBiaya();
                o[7] = p.getDp();
                o[8] = p.getStatusPinjam();
                o[9] = p.getStatusBayar();
                o[10] = p.getStatusDP();
                tabelModel.addRow(o);
            }
            this.tabelPeminjaman.setModel(tabelModel);
        }
    }
    
