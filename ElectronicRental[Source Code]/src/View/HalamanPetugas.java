    private void calculate_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculate_btnActionPerformed
        // TODO add your handling code here:
        String barang = (String) this.barang_cb.getSelectedItem();
        String member = (String) this.member_cb.getSelectedItem();
        String[] barang_split = barang.split(" - ", 3);
        String[] member_split = member.split(" - ", 2);
        String pinjam = this.pinjam_tf.getText();
        int durasi = Integer.valueOf(this.durasi_tf.getText());
        int biaya  = durasi * Integer.valueOf(barang_split[2]);
        int dp = (int) (0.1 * biaya);

        Date pinjamDate = null;
        try {
            pinjamDate = new SimpleDateFormat("yyyy-MM-dd").parse(pinjam);
        } catch (ParseException ex) {
            Logger.getLogger(HalamanPetugas.class.getName()).log(Level.SEVERE, null, ex);
        }

        Calendar c = Calendar.getInstance();
        c.setTime(pinjamDate);
        c.add(Calendar.DATE, durasi);
        Date kembaliDate = c.getTime();
        String kembali = String.valueOf(dateFormat.format(kembaliDate));

        this.kembali_tf.setText(kembali);
        this.total_tf.setText(String.valueOf(biaya));
        this.dp_tf.setText(String.valueOf(dp));
    }//GEN-LAST:event_calculate_btnActionPerformed

